#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import crypto from "node:crypto";
import readline from "node:readline/promises";
import { stdin as input, stdout as output } from "node:process";
import dotenv from "dotenv";
import pg from "pg";
import { createRequire } from "node:module";

dotenv.config();

const require = createRequire(import.meta.url);
const { buildPgClientConfig } = require("./lib/pg-connection-options.cjs");
const { Client } = pg;

const ROOT = path.resolve(".");
const MIGRATIONS_DIR = path.join(ROOT, "db", "migrations");
const MIGRATION_FILENAME = /^\d{4}[a-z]?_.+\.sql$/i;

function parseArgs(argv) {
  const out = { yes: false, allowCi: false, output: null };
  for (let i = 0; i < argv.length; i += 1) {
    const token = argv[i];
    if (token === "--yes") out.yes = true;
    if (token === "--allow-ci") out.allowCi = true;
    if (token === "--output" && argv[i + 1]) {
      out.output = argv[i + 1];
      i += 1;
    }
  }
  return out;
}

function fail(message) {
  console.error(`runbook:replay-missing-migrations FAIL\n- ${message}`);
  process.exit(1);
}

function sha256(text) {
  return crypto.createHash("sha256").update(text, "utf8").digest("hex");
}

function sqlLiteral(value) {
  return `'${String(value).replace(/'/g, "''")}'`;
}

function stripTransactionWrappers(sql) {
  const lines = sql.split(/\r?\n/);
  const beginIdx = lines.findIndex((line) => /^\s*BEGIN;\s*$/i.test(line));
  if (beginIdx >= 0) lines.splice(beginIdx, 1);
  for (let i = lines.length - 1; i >= 0; i -= 1) {
    if (/^\s*COMMIT;\s*$/i.test(lines[i])) {
      lines.splice(i, 1);
      break;
    }
    if (lines[i].trim() !== "") break;
  }
  return `${lines.join("\n").replace(/\s+$/, "")}\n`;
}

function defaultOutputPath() {
  const stamp = new Date().toISOString().replace(/[:.]/g, "-");
  return path.join(ROOT, "scripts", `safe-replay-template-${stamp}.sql`);
}

const args = parseArgs(process.argv.slice(2));

if (process.env.CI === "true" && !args.allowCi) {
  fail("this helper is interactive and must not run in CI (use --allow-ci only for diagnostics)");
}
if (!fs.existsSync(MIGRATIONS_DIR)) fail("missing db/migrations directory");

const connectionString = process.env.DATABASE_DIRECT_URL || process.env.DATABASE_URL;
if (!connectionString) fail("DATABASE_DIRECT_URL or DATABASE_URL is required");

const diskMigrations = fs
  .readdirSync(MIGRATIONS_DIR)
  .filter((name) => MIGRATION_FILENAME.test(name))
  .sort((a, b) => a.localeCompare(b));

const diskSet = new Set(diskMigrations);
const client = new Client(buildPgClientConfig(connectionString));
await client.connect();

try {
  const canonicalRows = await client.query(`SELECT filename FROM _system._schema_migrations ORDER BY filename ASC`);
  const mirrorRows = await client.query(`SELECT name FROM ih35_migrations.applied_migrations ORDER BY name ASC`);

  const canonicalSet = new Set(canonicalRows.rows.map((row) => String(row.filename)));
  const mirrorSet = new Set(mirrorRows.rows.map((row) => String(row.name)));

  const missingInCanonical = [...diskSet].filter((name) => !canonicalSet.has(name)).sort((a, b) => a.localeCompare(b));
  const missingInMirror = [...diskSet].filter((name) => !mirrorSet.has(name)).sort((a, b) => a.localeCompare(b));
  const replayCandidates = [...new Set([...missingInCanonical, ...missingInMirror])].sort((a, b) => a.localeCompare(b));

  if (replayCandidates.length === 0) {
    console.log("No missing migrations detected. Replay not required.");
    process.exit(0);
  }

  console.log(`Replay candidates: ${replayCandidates.length}`);
  for (const file of replayCandidates) {
    const tags = [];
    if (missingInCanonical.includes(file)) tags.push("missing-canonical");
    if (missingInMirror.includes(file)) tags.push("missing-mirror");
    console.log(`  - ${file} [${tags.join(", ")}]`);
  }

  console.log("\nRunbook checkpoints:");
  console.log("  1) Backup/PITR restore point confirmed.");
  console.log("  2) Drift snapshot captured.");
  console.log("  3) Privileged orphan cleanup (if needed) is separate:");
  console.log("     docs/runbooks/migration-orphan-cleanup.md");

  if (!args.yes) {
    const rl = readline.createInterface({ input, output });
    const response = (await rl.question("\nGenerate SAFE_REPLAY SQL template now? [y/N] ")).trim().toLowerCase();
    rl.close();
    if (response !== "y" && response !== "yes") {
      console.log("Aborted without writing replay SQL template.");
      process.exit(0);
    }
  }

  const targetFile = path.resolve(args.output ?? defaultOutputPath());
  const operator = process.env.USER || process.env.LOGNAME || "operator";
  const out = [];

  out.push("-- SAFE_REPLAY template generated by runbook-replay-missing-migrations.mjs");
  out.push(`-- Generated at: ${new Date().toISOString()}`);
  out.push("-- Source runbook: docs/runbooks/SAFE-REPLAY-PROCEDURE.md");
  out.push("-- Review manually before execution. Do not run blindly.");
  out.push("BEGIN;");
  out.push("");

  for (const filename of replayCandidates) {
    const migrationPath = path.join(MIGRATIONS_DIR, filename);
    const raw = fs.readFileSync(migrationPath, "utf8");
    const checksum = sha256(raw);
    const body = stripTransactionWrappers(raw).trimEnd();

    out.push(`-- >>> ${filename}`);
    out.push(`-- checksum(sha256): ${checksum}`);
    out.push(body);
    out.push("");
    out.push("INSERT INTO _system._schema_migrations (filename, checksum, applied_at, applied_by, duration_ms)");
    out.push(
      `VALUES (${sqlLiteral(filename)}, ${sqlLiteral(checksum)}, now(), ${sqlLiteral(operator)}, 0) ON CONFLICT (filename) DO NOTHING;`
    );
    out.push("INSERT INTO ih35_migrations.applied_migrations (name, applied_at, applied_by)");
    out.push(`VALUES (${sqlLiteral(filename)}, now(), ${sqlLiteral(operator)}) ON CONFLICT (name) DO NOTHING;`);
    out.push("");
  }

  out.push("COMMIT;");
  out.push("");

  fs.writeFileSync(targetFile, out.join("\n"), "utf8");
  console.log(`Wrote SAFE_REPLAY template: ${path.relative(ROOT, targetFile)}`);
  console.log("Next: review SQL, run in controlled window, then verify drift + consistency.");
} catch (error) {
  fail(String(error?.message ?? error));
} finally {
  await client.end();
}
